            <h1>Video conferencing etiquette: 10 tips for a successful video conference</h1>
            <p>Article taken from <cite><a href="https://resources.owllabs.com"> resources.owllabs.com </a></cite></p>
            <time datetime="2023-04-07 20:30">2023-04-07 20:30</time>

            <img src="../views/images/video_conference.png" alt="video conference picture">
            <p>Video conferencing has become an essential component of the modern business world and helps improve the connectedness of remote team members.</p>
            <p>In fact, 80% of executives say video conferencing is taking over audio conferencing as the go-to form of internal team communication, and 84% think it is outpacing audio conferencing in communicating with external stakeholders. That, combined with the fact that working remotely has become an increasingly dominant workforce trend means that understanding video conference protocols is more important than ever.</p>
            <p>If you aren't completely familiar with the additional expectations placed upon you when you're a part of a business video conference call, that's okay. We're here to help you learn the dos and don'ts of video conferencing call etiquette. Take the following information into consideration so you don't find yourself committing a major faux pas at the next big meeting. Or, share this with a coworker to help them out the next time they're making one of these mistakes. Also, be sure to check out our list of the best video conferencing platforms to help you make the right selection for your next video conference.</p>
            <h2>Video Conferencing Etiquette</h2>
            <ol>
                <li>Mute yourself when not speaking.</li>
                <li>Be on time.</li>
                <li>Ensure your technology works correctly.</li>
                <li>Use technology to fully engage remote participants.</li>
                <li>Choose the proper software and hardware.</li>
                <li>Wear work-appropriate clothing.</li>
                <li>Frame the camera correctly.</li>
                <li>Have the right light.</li>
                <li>Look into the camera.</li>
                <li>Pay attention.</li>
            </ol>

            <h2>1. Mute yourself when not speaking.</h2>
            <p>Even though you may not be speaking and think you're being quiet, most microphones can pick up minor background noises, like coughs, sneezes, or typing. These sounds can easily distract other video conferencing participants and potentially even cause annoyance.</p>
            <p>Let's say you're typing during the meeting to try and get work done. While you may be able to get away with it, you might not want your boss calling you out for not paying attention. Make it a practice (out of common courtesy to your colleagues) to mute yourself whenever you're not talking. For most video conferencing software, it's as simple as a click of a button.</p>
            
            <h2>2. Be on time.</h2>
            <p>This one should be standard with any meeting, video or otherwise. However, when you're dialing in to a video conference, it's especially important. While you might be able to get away with sneaking into a physical meeting late, everything is more visible in a video conference.</p>
            <p>Eye contact is extremely important during a video conference, as you want the person or team that's conferencing in to feel engaged. When you walk in late, you'll be making noise and distract anyone who is speaking in the room. This can result in confusion and stoppages. Additionally, when you're on time for a meeting, it'll make getting set up with technology easier and less painless so the meeting can start on time.</p>

            <h2>3. Ensure your technology works correctly.</h2>
            <p>You don't want to have to delay a meeting with an important client because your video conferencing system isn't working properly. You need to do a few test runs with internal employees before trying to land the next big investor. Find someone willing to help, and make sure you understand the process fully before starting your first video conference. This will make sure everything runs smoothly during the real thing.</p>

            <h2>4. Use technology to fully engage remote participants.</h2>  
            <p>You want your remote video conference attendees to feel like they can participate and are truly a part of the meeting. The 2020 State of Remote Work report found that interruptions and being talked over are two of the biggest meeting challenges for both remote and on-site workers.</p>
            <p>Luckily, there are newer and smarter hardware options than ever that can make video conferences run smoother. Our very own Meeting Owl Pro is a 360º camera, microphone, and speaker that uses smart technology to visually highlight the active speaker while simultaneously showing a panorama of the room. This creates an organic, conversational atmosphere that engages remote participants far more than a simple webcam setup.</p>

            <h2>5. Choose the proper software and hardware.</h2>
            <p>Having the right video conferencing hardware is only half the equation. You'll also need a software solution that your employees can use without frustration or extensive training. If you're looking for new software, check out our video conferencing comparison guide for reviews, features, and recommendations.</p>
        
            <h2>6. Wear work-appropriate clothing.</h2>
            <p>While it might be tempting to work in your favorite sweatshirt all day, consider wearing professional attire to any video conferences you're attending. You don't have to wear anything fancy, but choose something that would be appropriate if the meeting were face-to-face, rather than virtual.</p>

            <h2>7. Frame the camera correctly.</h2>
            <p>We've all been on video calls where we end up looking up people's nostrils or seeing the side of their face. When you're on video, make sure you frame your camera in a way that feels natural and allows you to look at the camera. Sit at eye level to the lens, and try to position yourself so that it shows midsection up. Placing it too high leaves other participants staring down at you like a bad tv show. Putting a camera too low can lead to unflattering and awkward angles.</p>
            
            <h2>8. Have the right light.</h2>
            <p>Poor lighting conditions have an enormous effect on the video quality that you send. You'll want to make sure that there is enough light in the room you're in so that your video isn't grainy and unwatchable. Try to not mix natural lighting and office lighting unless your office bulbs are daylight white. You also don't want any faces being lit from below, as this makes you look like a cartoonish villain from a silent film. Lighting from the sides will make faces look the best, so try for that if you have the ability.</p>

            <h2>9. Look into the camera.</h2>
            <p>A common mistake is looking at the video feed instead of the camera when speaking to a remote participant. While it may seem like the right thing to do, it actually makes it appear as if you're looking off and not paying attention. This will make you come across as more aloof and less professional. Looking into the camera lens is the equivalent of looking into the person's eyes, so practice doing so until you're comfortable with it.</p>

            <h2>10. Pay attention.</h2>
            <p>Stop checking emails or working on your PowerPoint presentation during video conferences. Not only does research suggest only 3% of people can multitask effectively, but you also look rude to your participants.</p>
